function getErrorMessage(errorcode, lang)
{
	// error code 0 : network disconnected
	// error code 1 : any other network error
	var temp = netError[errorcode][lang];		
	if(temp == null)
		temp = netError[errorcode]['en'];	
	return temp;
}
